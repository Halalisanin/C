#include <stdio.h>

/**
 * main - Entry point of the program
 *
 * Description:  This program demonstrates increment and decrement operatins
 *
 * Return: Always 0 (Success)
 *
 */
int main(void)

{

int n1 = 5, n2 = 4;

/** increase value of n1 by 1 */
++n1;

/** decrease value of n2 by 1 */
--n2;

printf("n1 = %d\n", n1);
printf("n2 = %d\n", n2);

return (0);

}
